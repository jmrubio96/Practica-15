    private String getSelectedRadioButton(){
        String option = "";
        
        if(infantil.isSelected()){
            option = "Infantil";
        }else if (mayor7.isSelected()){
            option = "Mayores de 7 años";
        }else if (mayor12.isSelected()){
            option = "Mayores de 12 años";
        }else if (adultos.isSelected()){
            option = "Mayores de 18 años";
        }
        return option;
    }
