    private ArrayList<String> getSelectedCheckBoxes(){
        ArrayList<String> options = new ArrayList<>();
        
        if(fantasia.isSelected()){
            options.add("Fantasía");
        }if (terror.isSelected()){
            options.add("Terror");
        }if (comedia.isSelected()){
            options.add("Comedia");
        }if (romance.isSelected()){
            options.add("romance");
        }
        
        return options;
    }
    
    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
                    // TODO add your handling code here:
        Book book = new Book();
        
        book.setTitle(insertt.getText());
        book.setAuthor(inserta.getText());
        book.setEditorial(editorial.getSelectedItem().toString());
        book.setAge(getSelectedRadioButton());
        book.setGenres(getSelectedCheckBoxes());
        
        model = (DefaultTableModel)table.getModel();
        model.addRow(book.toArray());
        
    }//GEN-LAST:event_addActionPerformed

